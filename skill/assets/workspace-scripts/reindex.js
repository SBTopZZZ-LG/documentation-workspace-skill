#!/usr/bin/env node
// reindex.js — regenerate the auto-index section in README.md.
// Reads <workspace>/index.yaml and replaces the contents of the
// <!-- BEGIN AUTO-INDEX --> ... <!-- END AUTO-INDEX --> block in README.md.
//
// Usage: node scripts/reindex.js

const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const workspaceRoot = path.resolve(__dirname, '..');
const readmePath = path.join(workspaceRoot, 'README.md');
const indexPath = path.join(workspaceRoot, 'index.yaml');

if (!fs.existsSync(readmePath)) {
  console.error('README.md not found at workspace root.');
  process.exit(1);
}

if (!fs.existsSync(indexPath)) {
  console.error('index.yaml not found at workspace root.');
  process.exit(1);
}

const indexData = yaml.load(fs.readFileSync(indexPath, 'utf8')) || {};
const documents = Array.isArray(indexData.documents) ? indexData.documents : [];

let table = '| Path | Title | Summary |\n';
table += '|---|---|---|\n';

if (documents.length === 0) {
  table += '| _(no documents yet — populate `index.yaml` as the workspace grows)_ | | |\n';
} else {
  for (const doc of documents) {
    if (!doc || typeof doc !== 'object' || !doc.path) continue;
    const path_ = String(doc.path);
    const title = doc.title ? String(doc.title) : path_;
    const summary = doc.summary ? String(doc.summary) : '';
    table += `| ${path_} | ${title} | ${summary} |\n`;
  }
}

const readme = fs.readFileSync(readmePath, 'utf8');
const beginMarker = '<!-- BEGIN AUTO-INDEX -->';
const endMarker = '<!-- END AUTO-INDEX -->';
const beginIdx = readme.indexOf(beginMarker);
const endIdx = readme.indexOf(endMarker);

if (beginIdx === -1 || endIdx === -1 || endIdx < beginIdx) {
  console.error('Auto-index markers not found in README.md. Add them and try again.');
  process.exit(1);
}

const newBlock = [
  beginMarker,
  '<!-- This section is auto-generated by `npm run reindex`. Do not edit by hand. -->',
  '<!-- Each entry below is sourced from index.yaml. -->',
  '',
  table.trimEnd(),
  endMarker,
].join('\n');

const newReadme = readme.slice(0, beginIdx) + newBlock + readme.slice(endIdx + endMarker.length);
fs.writeFileSync(readmePath, newReadme);

console.log(`README.md auto-index regenerated (${documents.length} document${documents.length === 1 ? '' : 's'}).`);
